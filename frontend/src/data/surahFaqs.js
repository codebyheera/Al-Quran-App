function normalizeFaqText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

export function getSurahFaqs(surahNumber) {
  if (!surahNumber) return [];

  return (surahFaqs[surahNumber] || [])
    .map((item) => ({
      question: normalizeFaqText(item?.question),
      answer: normalizeFaqText(item?.answer),
    }))
    .filter((item) => item.question && item.answer);
}

export function getSurahFaqSchema({
  surahNumber,
  pageUrl,
  pageTitle,
  inLanguage = "en",
} = {}) {
  const items = getSurahFaqs(surahNumber);

  if (!items.length) return null;

  const normalizedUrl = normalizeFaqText(pageUrl);
  const normalizedTitle = normalizeFaqText(pageTitle);

  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "@id": normalizedUrl ? `${normalizedUrl}#faq` : undefined,
    url: normalizedUrl || undefined,
    name: normalizedTitle || undefined,
    inLanguage,
    mainEntity: items.map((item, index) => ({
      "@type": "Question",
      "@id": normalizedUrl ? `${normalizedUrl}#faq-question-${index + 1}` : undefined,
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  };
}
