// title/description built from live Surah data (name, translation, verse count, revelation).
export function getSurahSeo(surahNum, surah) {
  const override = surahSeo[surahNum];
  if (override) return override;
  if (!surah) return null;
  return {
    title: `Surah ${surah.surahName} (${surah.nameTranslation}) - ${surah.arabicName} - Read Online`,
    description: `Read and listen to Surah ${surah.surahName} (${surah.nameTranslation}). Contains ${surah.versesCount} verses. Revealed in ${surah.revelation}. Arabic text, translation and audio available.`,
  };
}
